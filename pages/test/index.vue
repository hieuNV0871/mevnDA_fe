    'ý': 'y', 'ỳ': 'y', 'ỷ': 'y', 'ỹ': 'y', 'ỵ': 'y',
    'đ': 'd',
  };

  return str.replace(/[^\u0000-\u007E]/g, char => diacriticsMap[char] || char);
}
